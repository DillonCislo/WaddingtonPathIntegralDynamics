    linspace(yLim(1), yLim(2), numGridPts));

% Initial density
sigma = 0.1;
initDensity = (X-fixedPts(1,1)).^2 + (Y-fixedPts(1,2)).^2;
initDensity = exp(-initDensity / (2 * sigma.^2));
initDensity = initDensity ./ (2*pi*(sigma^2))^(1/2);

FPU = reshape(UFunc(X(:), Y(:)), size(X)) ./ scalarMetric; 
timeSpan = [0 5]; % Time span for integration

FPSol = solveFokkerPlanck2DFD(X, Y, initDensity, ...
    'U', reshape(FPU, size(X)), ...
    'DirichletBoundaryConditions', [], 'TimeSpan', timeSpan, ...
    'FokkerPlanckType', 'Forward', 'IntegratorType', 'non-stiff', ...
    'D', D, 'Display', 'iter');

clear numGridPts % sigma

%% View Results ===========================================================
close all; clc;

