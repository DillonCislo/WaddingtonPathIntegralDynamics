equiDensity = reshape(exp(-FPU./D), size(X));
equiDensity = equiDensity ./ sum(equiDensity(:)) .* sum(initDensity(:));

sinkIDx = fpLambda < 0;
saddleIDx = fpLambda >= 0;

for tidx = 1:numTimePoints
    
